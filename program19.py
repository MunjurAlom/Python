print("break and continue keywords")

i = 1
while i <= 30:
    if i == 15:
        break
    print(i)
    i = i+1
print("Ended Program")
print("----------------")
print("Continue keyword")

j = 1
while j <= 30:
    if j == 15:
        continue
    print(j)
    j = j+1
print("Program End")
